/* ------------------------------------------------------------
   根据星曜名称获取辅星信息
   ------------------------------------------------------------ */

export function getMinorStarInfo(starName: string): MinorStarInfo | undefined {
  return MINOR_STARS[starName]
}

/* ------------------------------------------------------------
   获取指定类别的辅星列表
   ------------------------------------------------------------ */

export function getMinorStarsByCategory(category: '六吉星' | '六煞星' | '其他辅星'): MinorStarInfo[] {
  return Object.values(MINOR_STARS).filter(star => star.category === category)
}

/* ------------------------------------------------------------
   获取所有辅星
   ------------------------------------------------------------ */

export function getAllMinorStars(): MinorStarInfo[] {
  return Object.values(MINOR_STARS)
}
